package aula08;

public class Endereco {
	String logradouro;
	int numero;
	String bairro;
	String cidade;
	String uf;
	
	public Endereco(String logradouro, int numero, String bairro, String cidade, String uf) {
		super();
		this.logradouro = logradouro;
		this.numero = numero;
		this.bairro = bairro;
		this.cidade = cidade;
		this.uf = uf;
	}
	
}
